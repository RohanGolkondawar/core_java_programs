public class DifferentLiterals {
    public static void main(String[] args){
        int integerVariable = 100;
        float floatVariable = 99.99F;
        char characterVariable = 'A';
        String stringVariable = "Hello Java";
        boolean booleanVariable = true;
        System.out.println(integerVariable);
        System.out.println(floatVariable);
        System.out.println(characterVariable);
        System.out.println(stringVariable);
        System.out.println(booleanVariable);

    }
}